const BOOTSTRAP_LTR = "https://cdnjs.cloudflare.com/ajax/libs/bootstrap/5.3.3/css/bootstrap.min.css";
const BOOTSTRAP_RTL = "https://cdnjs.cloudflare.com/ajax/libs/bootstrap/5.3.3/css/bootstrap.rtl.min.css";

function applyLanguage(lang) {
  const dict = translations[lang];
  if (!dict) return;

  // 1) swap text content
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  // 2) swap placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key] !== undefined) el.setAttribute("placeholder", dict[key]);
  });

  // 3) direction + lang + bootstrap stylesheet
  const html = document.documentElement;
  html.lang = lang;
  html.dir = lang === "ar" ? "rtl" : "ltr";

  const bsLink = document.getElementById("bootstrapCss");
  if (bsLink) bsLink.href = lang === "ar" ? BOOTSTRAP_RTL : BOOTSTRAP_LTR;

  // 4) update the toggle button label (shows a single-letter shortcut for the OTHER language)
  const toggleBtn = document.getElementById("langToggle");
  if (toggleBtn) {
  const showingArabicLetter = lang !== "ar";
  toggleBtn.textContent = showingArabicLetter ? "ع" : "A";
  toggleBtn.classList.toggle("is-ain", showingArabicLetter);
}

  localStorage.setItem("gtech_lang", lang);
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("gtech_lang") || "en";
  applyLanguage(saved);

  const toggleBtn = document.getElementById("langToggle");
  if (toggleBtn) {
    toggleBtn.addEventListener("click", () => {
      const current = document.documentElement.lang === "ar" ? "ar" : "en";
      applyLanguage(current === "ar" ? "en" : "ar");
    });
  }
});
